"""
Faça um programa em python no qual o usuário informa um número inteiro e o programa
exibe como saída seu antecessor e seu sucessor.
"""

numeroInteiro = int(input("Digite o número inteiro: "))

antecessor = numeroInteiro - 1
sucessor = numeroInteiro + 1
print("\n")
print("Número informado: ", numeroInteiro)
print("O número antecessor é: ", antecessor)
print("O número sucessor é: ", sucessor)

