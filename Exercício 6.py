"""
Faça um programa em python para ler os dados de um usuário: nome, telefone e endereço.
Ao final, o programa deve imprimir na tela um relatório com os dados do usuário.
"""

nome = input("Informe o nome do usuário: ")
telefone = input("Informe o telefone do usuário: ")
endereço = input("Informe o endereço do usuário: ")
print("\n")
print("Dados do usuário: -Nome: ", nome, "-Telefone: ", telefone, "-Endereço: ", endereço)
